export function generateWords(count = 50, difficulty = 'medium', textMode = 'words') {
  if (textMode === 'quotes') {
    // Import handled at call site
    return [];
  }

  if (textMode === 'code') {
    return generateCodeWords(count);
  }

  let wordList;
  switch (difficulty) {
    case 'easy':
      wordList = easyWords;
      break;
    case 'hard':
      wordList = hardWords;
      break;
    case 'medium':
    default:
      wordList = mediumWords;
      break;
  }

  const result = [];
  for (let i = 0; i < count; i++) {
    result.push(wordList[Math.floor(Math.random() * wordList.length)]);
  }
  return result;
}

function generateCodeWords(targetCount) {
  const result = [];
  const shuffled = [...codeSnippets].sort(() => Math.random() - 0.5);

  for (const snippet of shuffled) {
    const words = snippet.split(/\s+/);
    result.push(...words);
    if (result.length >= targetCount) break;
  }

  // If we need more, repeat
  while (result.length < targetCount) {
    const snippet = codeSnippets[Math.floor(Math.random() * codeSnippets.length)];
    result.push(...snippet.split(/\s+/));
  }

  return result.slice(0, Math.max(targetCount, result.length));
}

export function getWordCount(seconds) {
  if (seconds <= 15) return 40;
  if (seconds <= 30) return 70;
  if (seconds <= 60) return 120;
  return Math.ceil(seconds * 2.2);
}
